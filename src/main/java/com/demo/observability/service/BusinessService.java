package com.demo.observability.service;

import java.util.Map;

/**
 * Интерфейс основного бизнес-сервиса.
 *
 * ЗАЧЕМ ИНТЕРФЕЙС?
 * В Spring все зависимости должны инжектироваться через интерфейсы, а не через
 * конкретные классы. Это позволяет:
 * 1. Spring AOP создавать proxy-объекты поверх реализации (нужно для @Observed, @Transactional и др.)
 * 2. Легко подменять реализацию в тестах (mock)
 * 3. Соблюдать принцип инверсии зависимостей (DIP из SOLID)
 *
 * Контроллер знает только об этом интерфейсе — он не зависит от конкретного DemoService.
 */
public interface BusinessService {
    /**
     * Запускает полный бизнес-процесс и возвращает агрегированный результат всех этапов.
     */
    Map<String, Object> process();
}
